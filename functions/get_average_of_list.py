def average_list(list_containing_numbers):
    """
    Henter et gjennomsnitt av en liste. Ikke i bruk i programmet per nÃ¥.
    :param list_containing_numbers: Liste med tall
    :return: Gjennomsnittet av listen
    """
    average = sum(list_containing_numbers) / len(list_containing_numbers)
    return average
