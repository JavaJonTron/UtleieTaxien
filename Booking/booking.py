class Booking:
    def __init__(self, renter, year_from, month_from, day_from, year_to, month_to, day_to, car):
        self.renter = renter
        self.year_from = year_from
        self.month_from = month_from
        self.day_from = day_from
        self.year_to = year_to
        self.month_to = month_to
        self.day_to = day_to
        self.car = car

